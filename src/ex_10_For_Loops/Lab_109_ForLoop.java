package ex_10_For_Loops;

public class Lab_109_ForLoop {
    public static void main(String[] args) {
        for(int i=0;i<=10;i++)
        {
            System.out.println(i);  //Loop will be executed 11 times 0 to 10
        }

        for(int j=1;j<=10;j++)
        {
            System.out.println(j);//Loop executed 10 times, 1 to 10 will be printed
        }
    }
}

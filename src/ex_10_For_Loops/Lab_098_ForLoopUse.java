package ex_10_For_Loops;

public class Lab_098_ForLoopUse {
    public static void main(String[] args) {
        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation(Incre/decre)
        // ICU

        for(int i=1;i<=10;i++)
        {
            System.out.println(i);
        }
    }
}

package ex_04_Operators;

public class Lab054_Interview {
    public static void main(String[] args) {
        char a1 = 'A'; // ASCII -> 65
        char b1 = 'B'; // 66
        System.out.println(a1); // A
        System.out.println(b1); // B
        System.out.println(a1 + b1); //65+66=131
    }
}
